def show_why_log():
    print("--- ğŸ§  Decision & Context Log ---")
    with sqlite3.connect(COMPASS_DB) as conn:
        logs = conn.execute("SELECT content, reason FROM context_log WHERE reason IS NOT NULL ORDER BY id DESC").fetchall()
